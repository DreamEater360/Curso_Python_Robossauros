# Logica booleana

x = True
y = False

# Operador AND
print(x and y)  # False
print(True and True)  # True
print(False and False)  # False

# Operador OR
print(x or y)  # True
print(True or False)  # True
print(False or False)  # False

# Operador NOT
print(not x)  # False
print(not y)  # True

# Exemplos de operadores de comparação
x = 5
y = 10

print(x >= y)  # False, maior igual
print(x <= y)  # True, menor igual
print(x == y)  # False, igual
print(x != y)  # True, diferente
print(x < y)   # True, menor
print(x > y)   # False, maior
